package com.eunbyeol.individaul.model;

public class FileListDTO {
	
	private String flie_id;
	private String flie_name;
	private String flie_route;
	private int flie_order;
	private String flie_use_yn;
	
	public String getFlie_id() {
		return flie_id;
	}
	public void setFlie_id(String flie_id) {
		this.flie_id = flie_id;
	}
	public String getFlie_name() {
		return flie_name;
	}
	public void setFlie_name(String flie_name) {
		this.flie_name = flie_name;
	}
	public String getFlie_route() {
		return flie_route;
	}
	public void setFlie_route(String flie_route) {
		this.flie_route = flie_route;
	}
	public int getFlie_order() {
		return flie_order;
	}
	public void setFlie_order(int flie_order) {
		this.flie_order = flie_order;
	}
	public String getFlie_use_yn() {
		return flie_use_yn;
	}
	public void setFlie_use_yn(String flie_use_yn) {
		this.flie_use_yn = flie_use_yn;
	}
	
}
