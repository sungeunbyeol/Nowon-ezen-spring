package com.eunbyeol.individaul.model;

public class PaintingDTO {

	private int paint_no;
	private String flie_id;
	private String paint_division;
	private String paint_author;
	private String paint_name;
	
	public int getPaint_no() {
		return paint_no;
	}
	public void setPaint_no(int paint_no) {
		this.paint_no = paint_no;
	}
	public String getFlie_id() {
		return flie_id;
	}
	public void setFlie_id(String flie_id) {
		this.flie_id = flie_id;
	}
	public String getPaint_division() {
		return paint_division;
	}
	public void setPaint_division(String paint_division) {
		this.paint_division = paint_division;
	}
	public String getPaint_author() {
		return paint_author;
	}
	public void setPaint_author(String paint_author) {
		this.paint_author = paint_author;
	}
	public String getPaint_name() {
		return paint_name;
	}
	public void setPaint_name(String paint_name) {
		this.paint_name = paint_name;
	}
	
}
