package com.eunbyeol.individaul.model;

public class CodeDTO {
	private int code_division;
	private String code_value;
	private String code_name;
	private int code_order;
	private String code_use;
	
	public int getCode_division() {
		return code_division;
	}
	public void setCode_division(int code_division) {
		this.code_division = code_division;
	}
	public String getCode_value() {
		return code_value;
	}
	public void setCode_value(String code_value) {
		this.code_value = code_value;
	}
	public String getCode_name() {
		return code_name;
	}
	public void setCode_name(String code_name) {
		this.code_name = code_name;
	}
	public int getCode_order() {
		return code_order;
	}
	public void setCode_order(int code_order) {
		this.code_order = code_order;
	}
	public String getCode_use() {
		return code_use;
	}
	public void setCode_use(String code_use) {
		this.code_use = code_use;
	}
	
	
}
